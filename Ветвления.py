# Функция для вычисления площади треугольной комнаты
def площадь_треугольной_комнаты(основание, высота):
    return 0.5 * основание * высота

# Функция для вычисления площади прямоугольной комнаты
def площадь_прямоугольной_комнаты(ширина, длина):
    return ширина * длина

# Функция для вычисления площади круглой комнаты
def площадь_круглой_комнаты(радиус):
    return 3.14 * (радиус ** 2)

